int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb == 0)
		return (0);
	if (nb > 2147483647)
		return (0);
	while (i * i < nb)
		i++;
	if ((i * i) % nb != 0)
		return (0);
	return (i);
}
